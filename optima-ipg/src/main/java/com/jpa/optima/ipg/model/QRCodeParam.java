package com.jpa.optima.ipg.model;

public class QRCodeParam {
	private String amount;
	private String city;
	private String fee;
	private String merchantCriteria;
	private String merchantID;
	private String merchantName;
	private String merchantPan;
	private String merchantTrxID;
	private String postalCode;
	private String partnerMerchantID;

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getFee() {
		return fee;
	}

	public void setFee(String fee) {
		this.fee = fee;
	}

	public String getMerchantCriteria() {
		return merchantCriteria;
	}

	public void setMerchantCriteria(String merchantCriteria) {
		this.merchantCriteria = merchantCriteria;
	}

	public String getMerchantID() {
		return merchantID;
	}

	public void setMerchantID(String merchantID) {
		this.merchantID = merchantID;
	}

	public String getMerchantName() {
		return merchantName;
	}

	public void setMerchantName(String merchantName) {
		this.merchantName = merchantName;
	}

	public String getMerchantPan() {
		return merchantPan;
	}

	public void setMerchantPan(String merchantPan) {
		this.merchantPan = merchantPan;
	}

	public String getMerchantTrxID() {
		return merchantTrxID;
	}

	public void setMerchantTrxID(String merchantTrxID) {
		this.merchantTrxID = merchantTrxID;
	}

	public String getPostalCode() {
		return postalCode;
	}

	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}

	public String getPartnerMerchantID() {
		return partnerMerchantID;
	}

	public void setPartnerMerchantID(String partnerMerchantID) {
		this.partnerMerchantID = partnerMerchantID;
	}

}
